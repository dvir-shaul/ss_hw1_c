#include "myMath.h"

float add(float x, float y){
    float i = 0;
    i = x + y;
    return i;
}
float sub(float x, float y){
    float i = 0;
    i = x -y;
    return i;
}
double mul(double x, int y){
    float i = 0;
    i = y*x;
    return i;
}
double div(double x, int y){
    float i = 0;
    i = x / y;
    return i;
}